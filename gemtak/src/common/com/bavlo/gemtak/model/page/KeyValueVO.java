public class KeyValueVO {

	//key
	private String pKey;
	//value
	private String pValue;
	//value_cn
	private String pValueCN;
	//value_en
	private String pValueEN;
	//desc_cn
	private String pDescCN;
	//desc_en
	private String pDescEN;
	
	
	public String getpKey() {
		return pKey;
	}
	public void setpKey(String pKey) {
		this.pKey = pKey;
	}
	public String getpValue() {
		return pValue;
	}
	public void setpValue(String pValue) {
		this.pValue = pValue;
	}
	public String getpValueCN() {
		return pValueCN;
	}
	public void setpValueCN(String pValueCN) {
		this.pValueCN = pValueCN;
	}
	public String getpValueEN() {
		return pValueEN;
	}
	public void setpValueEN(String pValueEN) {
		this.pValueEN = pValueEN;
	}
	public String getpDescCN() {
		return pDescCN;
	}
	public void setpDescCN(String pDescCN) {
		this.pDescCN = pDescCN;
	}
	public String getpDescEN() {
		return pDescEN;
	}
	public void setpDescEN(String pDescEN) {
		this.pDescEN = pDescEN;
	}
	
}
